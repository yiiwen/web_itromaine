      <p><strong>“不露面”的照片</strong></p>
      <p>引人入胜的设计元素之一是在图像中包含人脸，它可以帮助用户连接。</p>
      <p>那么为什么那么多的项目使用“不露面”照片呢？下面的每个例子都有人物的图像，但是没有人脸。</p>
      <p>这种风格的网站设计和照片选择有一个真正的目的：它允许用户在显示的场景中描绘自己。你可能是那个剪头发的人，也可能是几个在餐桌上开心吃饭的朋友之一。或者你可能只是被这一系列没有眼神交流的图片所吸引。</p>
      <p>当屏幕上出现人脸时，用户的眼睛会自然地停下来，几乎让眼睛满足于图像的主题（尤其是当图像和人脸很大的时候）。如果没有这个暂停（尽可能快地），用户将继续扫描整个设计。他们可能会看到一些他们在其他情况下看不到的东西，它可以给图像添加一个不同的焦点。</p>
      <p>虽然这种不带脸的照片风格并不适用于所有情况，但它是有效的。将这个概念用于不需要强烈情感牵引的更一般的想法。</p>
      <p>如果你正在计划一个没有面孔的设计，和摄影师进行对话，这样照片就会在脑海中形成。仅仅剪掉一个脑袋不会产生同样迷人的效果，反而会非常不和谐。</p>
      <p>匿名照片的另一个用途？如果你必须使用普通图片，这是一个有效的技巧。它可以隐藏太普通的模型，这样你的设计看起来就不会一成不变。</p>
      <p><strong>结论</strong></p>
      <p>视觉趋势的好处是你可以将它们整合到其他的趋势元素中，或者彼此结合。例如，黑白摄影，已经在所有的设计学科中使用了几十年；另外，你可以把它和一些很酷的用户体验效果结合起来，让这个项目真正脱颖而出。</p>
