      <p><strong><span>圆圈</span></strong></p>
      <p>用圆圈来设计并不新鲜，“完美”的形状让人赏心悦目，甚至是百度材料设计标准的一部分，你肯定注意到了所有那些圆形按钮。</p>
      <p>当谈到在网站设计项目中使用圆圈时，考虑这个形状的意义是很重要的。它可以为您的设计添加额外的内容层。</p>
      <p>康德在照片中使用圆圈，比起UI元素，它在你的脸上没那么显眼，但和谐和活力的光环就在那里。</p>
      <p> <img src="/images/share/15501371245580.jpg" alt=""> </p>
      <p>百度的年度游戏使用品牌圈来吸引用户点击游戏问题的答案，游戏中的圆圈模仿了材质设计界面中圆圈的颜色和大小，并再次强调了在百度的世界中，圆圈是用来互动的。</p>
      <p> <img src="/images/share/15501371517465.jpg" alt=""> </p>
      <p>Marijn W. Bankers使用一个圆形标志和一个巨大的圆圈来表示执行的工作类型，这两个圆都暗示着运动，标志圆是因为它是开着的，而较大的圆是因为彩色条以轮子的形式运动。</p>
